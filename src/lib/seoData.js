// 국가 소개문 선택: 손으로 쓴 주요국 소개가 있으면 그걸, 없으면(신규 국가 포함)
// 국가명·마커수·주요 도시명으로 자동 구성한다 → 어떤 국가가 추가돼도 빈 문구 없이 대응.
//   opts: { countryLabel, markerCount, cityNames:[상위 도시명 몇 개] }
export function getCountryIntro(countryUpper, opts = {}) {
  const hand = COUNTRY_INTRO[countryUpper];
  if (hand) return hand;

  const label = opts.countryLabel || countryUpper;
  const count =
    typeof opts.markerCount === "number" ? opts.markerCount : 0;
  const cities = Array.isArray(opts.cityNames)
    ? opts.cityNames.filter(Boolean).slice(0, 3)
    : [];
  const cityClause = cities.length ? `${cities.join("·")} 등 ` : "";

  if (count > 0) {
    return `${label}의 실시간 라이브캠 ${count}곳을 도시별로 모았습니다. ${cityClause}${label} 각지의 거리·명소·풍경을 지금 이 순간 생중계로 감상해 보세요.`;
  }
  return `${label}의 실시간 라이브캠을 준비 중입니다. 곧 ${label} 각지의 거리와 명소를 생중계로 만나보실 수 있습니다.`;
}

// 대륙 정규화: 레거시 continent="americas" 마커를 국가코드로 north/south america 재분류
// (getPublicMarkerById 등 단일 문서 조회 헬퍼에서도 동일 규칙을 적용해야 해서 export 한다)
export function normalizeContinent(m) {
  try {
    if (m && m.continent === "americas") {
      const c = getContinentByCountry(m.country);
      if (c) return { ...m, continent: c };
    }
  } catch (error) {
    // 정규화 실패 시 원본 그대로 사용
  }
  return m;
}

